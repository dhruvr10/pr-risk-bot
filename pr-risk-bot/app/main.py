def verify_signature(body: bytes, signature_header: Optional[str], secret: str) -> None:
    """Validate the GitHub HMAC-SHA256 webhook signature."""
    if not signature_header or not signature_header.startswith("sha256="):
        raise HTTPException(status_code=401, detail="Missing or invalid signature header")
    their_sig = signature_header.split("=", 1)[1].strip()
    our_sig = hmac.new(secret.encode(), msg=body, digestmod=hashlib.sha256).hexdigest()
    if not hmac.compare_digest(our_sig, their_sig):
        raise HTTPException(status_code=401, detail="Signature mismatch")


def make_comment(pr: Dict[str, Any], result: RiskResult) -> str:
    """Render the PR triage summary as a Markdown comment."""
    url    = pr.get("html_url", "")
    title  = pr.get("title", "")
    number = pr.get("number", "")
    user   = (pr.get("user") or {}).get("login", "unknown")

    lines: List[str] = [
        "## PR Triage Summary",
        f"- PR: [{title} #{number}]({url})",
        f"- Author: `{user}`",
        f"- Risk: **{result.score_10}/10 ({result.level})** — confidence: **{result.confidence}**",
        f"- Summary: {result.changed_files} files, {result.additions}+/{result.deletions}-",
        "",
        "### What changed (impact map)",
    ]

    lines += [f"- {item}" for item in result.impact_map[:8]] or ["- (Could not infer impact map)"]
    lines += [
        "",
        "### Risk drivers",
    ]
    lines += [f"{i}. {d}" for i, d in enumerate(result.risk_drivers[:6], 1)] or ["1. (No strong drivers detected)"]
    lines += [
        "",
        "### Suggested review focus",
    ]
    lines += [f"- {item}" for item in result.review_focus[:5]] or ["- (No specific review focus detected)"]
    lines += [
        "",
        "### Operational notes (if applicable)",
    ]
    lines += [f"- {n}" for n in result.operational_notes[:5]] or ["- None"]
    lines += [
        "",
        "> Generated by pr-risk-bot (heuristics-first).",
    ]

    return "\n".join(lines)


BOT_SIGNATURE = "<!-- pr-risk-bot -->"


def make_comment_with_signature(pr: Dict[str, Any], result: RiskResult) -> str:
    """Append a hidden HTML comment so we can find and update existing bot comments."""
    return make_comment(pr, result) + f"\n{BOT_SIGNATURE}"


