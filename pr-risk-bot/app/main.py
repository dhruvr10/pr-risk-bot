@app.post("/webhook/github")
async def github_webhook(
    request: Request,
    x_hub_signature_256: Optional[str] = Header(default=None),
    x_github_event: Optional[str] = Header(default=None),
) -> JSONResponse:
    body = await request.body()
    settings = request.app.state.settings
    gh: GitHubClient = request.app.state.gh

    verify_signature(body, x_hub_signature_256, settings.webhook_secret)

    try:
        payload: Dict[str, Any] = json.loads(body)
    except json.JSONDecodeError:
        raise HTTPException(status_code=400, detail="Invalid JSON payload")

    if x_github_event != "pull_request":
        return JSONResponse({"ok": True, "ignored": True, "reason": "not pull_request"})

    action = payload.get("action")
    if action not in {"opened", "reopened", "synchronize"}:
        return JSONResponse({"ok": True, "ignored": True, "reason": f"action={action}"})

    pr      = payload.get("pull_request") or {}
    repo    = payload.get("repository") or {}
    owner   = (repo.get("owner") or {}).get("login")
    repo_name = repo.get("name")
    pr_number = pr.get("number")

    if not (owner and repo_name and pr_number):
        raise HTTPException(status_code=400, detail="Missing repo/PR info in payload")

    logger.info("Processing PR #%s in %s/%s (action=%s)", pr_number, owner, repo_name, action)

    pr_full = await gh.get_pr(owner, repo_name, int(pr_number))
    files   = await gh.list_pr_files(owner, repo_name, int(pr_number))

    result  = compute_risk(files, pr_full)
    comment = make_comment_with_signature(pr_full, result)

    # Idempotency: update existing bot comment instead of posting a new one
    existing_id = await gh.find_bot_comment(owner, repo_name, int(pr_number), BOT_SIGNATURE)
    if existing_id:
        await gh.update_issue_comment(owner, repo_name, existing_id, comment)
        logger.info("Updated existing comment %s on PR #%s (risk=%s)", existing_id, pr_number, result.level)
    else:
        await gh.create_issue_comment(owner, repo_name, int(pr_number), comment)
        logger.info("Posted new comment on PR #%s (risk=%s)", pr_number, result.level)

    return JSONResponse({"ok": True, "posted": True, "risk": result.level, "score": result.score_10})


