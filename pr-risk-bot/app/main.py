# ---------------------------------------------------------------------------

@asynccontextmanager
async def lifespan(app: FastAPI):
    settings = load_settings()
    app.state.settings = settings
    app.state.gh = GitHubClient(
        token=settings.github_token,
        base_url=settings.base_url,
    )
    logger.info("pr-risk-bot started (base_url=%s)", settings.base_url)
    yield
    await app.state.gh.aclose()
    logger.info("pr-risk-bot shut down")


app = FastAPI(title="PR Risk Bot", version="1.0.0", lifespan=lifespan)


